def aumentar(n, porcentagem = 10):
    resultado10 = (n*porcentagem) / 100
    resultadof = n + resultado10
    return resultadof

def diminuir(n, porcentagem = 13):
    resultado13 = (n*porcentagem) / 100
    resultadof = n - resultado13
    return resultadof

def dobro(n):
    return n * 2

def metade(n):
    return n / 2